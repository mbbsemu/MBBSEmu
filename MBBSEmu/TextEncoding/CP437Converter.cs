        /// <summary>
        ///     Converts CP437 bytes to UTF-8 encoded bytes.
        ///     C0 control characters (0x00-0x1F) and DEL (0x7F) pass through as control codes.
        ///     Standard ASCII (0x20-0x7E) passes through unchanged.
        ///     Extended ASCII (0x80-0xFF) is converted to multi-byte UTF-8 sequences.
        ///
        ///     Low bytes are not converted to CP437 glyphs because BBS modules commonly
        ///     use them as control/formatting markers over telnet connections, not as
        ///     display characters. See issue #49 (T-LORD) for context.
        /// </summary>
        /// <param name="cp437Bytes">Input bytes in CP437 encoding</param>
        /// <returns>UTF-8 encoded bytes</returns>
        public static byte[] ConvertToUtf8(ReadOnlySpan<byte> cp437Bytes)
        {
            using var output = new MemoryStream(cp437Bytes.Length * 2);

            for (var i = 0; i < cp437Bytes.Length; i++)
            {
                var b = cp437Bytes[i];

                if (b < 0x80)
                {
                    // ASCII + control chars (0x00-0x7F) - pass through unchanged
                    output.WriteByte(b);
                }
                else
                {
                    // Extended ASCII (0x80-0xFF) - convert via lookup table
                    var unicodeChar = (char)CP437ToUnicode[b];
                    var utf8Bytes = Encoding.UTF8.GetBytes(new[] { unicodeChar });
                    output.Write(utf8Bytes, 0, utf8Bytes.Length);
                }
            }

            return output.ToArray();
        }

        /// <summary>
        ///     Gets the Unicode code point for a CP437 byte value.
        /// </summary>
        /// <param name="cp437Byte">CP437 byte value (0x00-0xFF)</param>
        /// <returns>Unicode code point</returns>
        public static ushort GetUnicodeCodePoint(byte cp437Byte) => CP437ToUnicode[cp437Byte];
    }
}
